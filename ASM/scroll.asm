TITLE to scroll and display the string in specified position
.MODEL SMALL
.STACK 32
.DATA
STRING DB 'PROGRAMMING IN ASSEMBLY IS FUN','$'
LEN DW $-STRING
INPUT  DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,06H    
MOV AL,10     ;scroll 10 lines up
MOV BH,00011111B
MOV CH,05
MOV CL,20
MOV DH,20
MOV DL,60
INT 10H

MOV DH,12
MOV DL,30
MOV AH,02H
MOV BH,0
INT 10H

MOV AH,09H
LEA DX,STRING
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
