TITLE to add from 1 to n and display in decimal form
.MODEL SMALL
.STACK 32
.DATA
STRING DB 'ENTER THE NUMBER IN THREE DIGIT FORM: ','$'
STRING2 DB 'THE SUM FROM 1 TO THE ENTERED NUMBER: ','$'
COUNT DB 0
NUM DB 0

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,00H
MOV AL,03H
INT 10H

MOV AH,09H
LEA DX,STRING
INT 21H

MOV CX,03
MOV SI,OFFSET NUM

STEP1:MOV AH,01
INT 21H
CMP AL,0DH       ;COMPAIRING WITH NEXT LINE
JE STEP2
SUB AL,30H       ;CONVERTING TO ASCII
PUSH AX
MOV AL,10
MUL NUM
MOV NUM,AL
POP AX
ADD NUM,AL
INC COUNT
STEP2:LOOPNE STEP1

MOV CX,[SI]
MOV DX,0
CALC:ADD DX,CX
LOOP CALC
MOV NUM,DL

MOV AH,02H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H

MOV AH,09H
LEA DX,STRING2
INT 21H

MOV CX,10
MOV AL,NUM
MOV AH,0
MOV BX,0

STEP3:MOV DX,0
DIV CX
ADD DX,30H
PUSH DX
INC BX
CMP AX,0
JA STEP3

MOV AH,02
MOV CX,BX
DISP:POP DX
INT 21H
LOOP DISP

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
