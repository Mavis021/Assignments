TITLE TO ADD THE 16 BIT NUMBERS AND DISPLAY THEIR SUM IN DECIMAL FORMAT
.MODEL SMALL
.STACK 32
.DATA
TEMP DW 00
NUMBERS DW 1111,2222,3333,4444,5555,6666,1111,1111,2222,2222,'$'

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,00H      ;clear screen
MOV AL,03H
INT 10H

;CALCULATION
MOV SI,OFFSET NUMBERS

MOV CX,0
MOV CL,10
MOV AX,0
MOV BX,0
AGAIN:ADD AX,[SI]
SKIP:ADD SI,2
LOOP AGAIN
JMP DISPLAY

;PLACE THE NUMBER TO BE CONVERTED IN AX
DISPLAY:MOV TEMP,AX     ;store in memory
CALL DISPLAY_DECIMAL

MOV AH,4CH
INT 21H

DISPLAY_DECIMAL PROC

MOV BX,10
STEP3:MOV DX,0
DIV BX
ADD DX,30H
PUSH DX
INC CX
CMP AX,0
JA STEP3

MOV AH,02
DISP:POP DX
INT 21H
LOOP DISP
RET
DISPLAY_DECIMAL ENDP

MAIN ENDP
END MAIN
