TITLE to convert the text in the memory into lowercase
.MODEL SMALL
.STACK 32
.DATA
STRING DB 'programming IS fUN','$'
LEN DW $-STRING

.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

MOV SI,OFFSET STRING
MOV CX,LEN
L1:MOV DL,[STRING+SI]
CMP DL,'A'
JB SKIP
CMP DL,'Z'
JA SKIP
ADD DL,20H
MOV [STRING+SI],DL
SKIP:INC SI
LOOP L1

MOV AH,09H
LEA DX,STRING
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
