TITLE to read a string and then display it in next line
.MODEL SMALL
.STACK 32
.DATA
STRING LABEL BYTE
MAXLEN  DB 20
ACCUTAL DB ?
INPUT   DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,0AH
LEA DX, STRING
INT 21H

;NEXT LINE
MOV AH,02H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H

MOV AH,09H
LEA DX, STRING+2
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MA
