TITLE TO COUNT THE NUMBER OF WORDS IN THE ENTERED SENTENCE
.MODEL SMALL
.STACK 32
.DATA
OUTPUT1 DB 'THERE ARE ','$'
OUTPUT2 DB  ' WORDS IN THE ENTERED SENTENCE','$'
COUNT DB 0
NUM DB 0
RES DW 00
STRING LABEL BYTE
MAXLEN DB 30
ACCTUAL DB ?
INPUT DB 30 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,00H      ;clear screen
MOV AL,03H
INT 10H

MOV AH,0AH
LEA DX,STRING
INT 21H

;CALCULATION
MOV SI,OFFSET STRING
MOV AX,0
MOV CH,0
MOV CL,ACCTUAL+2
AGAIN: MOV AL,[SI]
CMP AL,' '
JNE LEAVE
ADD COUNT,1
LEAVE:INC SI
LOOP AGAIN

MOV AH,02H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H

;DISPLAY RESULT
MOV AH,09H     
LEA DX,OUTPUT1
INT 21H

MOV AL,COUNT
MOV AH,0
;PLACE THE NUMBER TO BE CONVERTED IN AX
CALL DISPLAY_DECIMAL

MOV AH,09H
LEA DX,OUTPUT2
INT 21H

MOV AH,4CH
INT 21H

DISPLAY_DECIMAL PROC

MOV CX,10
MOV BX,0
STEP3:MOV DX,0
DIV CX
ADD DX,30H
PUSH DX
INC BX
CMP AX,0
JA STEP3

MOV AH,02
MOV CX,BX
DISP:POP DX
INT 21H
LOOP DISP
RET
DISPLAY_DECIMAL ENDP

MAIN ENDP
END MAIN
END
