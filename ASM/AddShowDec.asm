TITLE to add all the elements of a table, which are between 50 and 150 only,Display the result as the decimal value
.MODEL SMALL
.STACK 32
.DATA
NUM DB 0
NUMBERS DW 51,52,04,05,160,140,57,'$'
LEN DB $-NUMBERS

.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

MOV SI,OFFSET NUMBERS
MOV CH,0
MOV CL,LEN
MOV BX,0
MOV AX,0
STEP1:MOV DX,[NUMBERS+BX]
CMP DX,32H
JB SKIP
CMP DX,96H
JA SKIP
ADD AX,DX
SKIP: INC BX
LOOP STEP1

MOV NUM,AL
LEA SI,NUM
MOV BX,10
MOV CX,0
STEP2:MOV DX,0
DIV BX
ADD DX,30H
PUSH DX
INC CX
CMP AX,0
JNE STEP2

;DIAPLAY
MOV AH, 02
DISP:POP DX
INT 21H
LOOP DISP

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
