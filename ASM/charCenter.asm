TITLE to accept the string and display each character at center in new line
.MODEL SMALL
.STACK 32
.DATA
STRING LABEL BYTE
MAXLEN DB 20
ACCTUAL DB ?
INPUT  DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,0AH
LEA DX,STRING
INT 21H

MOV AH,00H
MOV AL,03H
INT 10H

MOV CH,0
MOV CL,ACCTUAL
MOV BX,2
MOV DH,11
STEP1:INC DH
MOV DL,30
MOV AH,02H
MOV BH,0
INT 10H
MOV DL,[STRING+BX]
INT 21H
INC BX
LOOP STEP1

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
