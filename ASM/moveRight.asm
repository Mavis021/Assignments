TITLE TO MOVE THE USER ENTERD STRING TOWARDS RIGHT
.MODEL SMALL
.STACK 32
.DATA
DEL DW 65535
LOCATION DW 0000H
STRING LABEL BYTE
MAXLEN DB 20
ACCTUAL DB ?
INPUT DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

CALL CLEAR_SCREEN
CALL GET_STRING
MOV CX,38
MOVE:CALL CLEAR_SCREEN
MOV DX,LOCATION
CALL SET_CURSOR
CALL DISPLAY_STRING
CALL DELAY
ADD LOCATION,02
LOOP MOVE
CALL END_PROG

END_PROG PROC
MOV LOCATION,0000H
MOV AH,4CH
INT 21H
RET
END_PROG ENDP

SET_CURSOR PROC
MOV AH,02H
MOV BH,00
INT 10H
RET
SET_CURSOR ENDP

CLEAR_SCREEN PROC
MOV AH,00H
MOV AL,03H
INT 10H
RET
CLEAR_SCREEN ENDP

DISPLAY_STRING PROC
DISPLAY:MOV AH,09H
LEA DX,STRING+2
INT 21H
RET
DISPLAY_STRING ENDP

GET_STRING PROC
MOV AH, 0AH
LEA DX,STRING
INT 21H
RET
GET_STRING ENDP

DELAY PROC
MOV AX,5
D2:MOV BX,DEL
D1:DEC BX
JNZ D1
DEC AX
JNZ D2
RET
DELAY ENDP

MAIN ENDP
END MAIN
