TITLE TO CONVERT INTO LOWERCASE AND DISPLAY AT THE LEFT CORNER OF THE DEFINED BOX
.MODEL SMALL
.STACK 32
.DATA
STRING LABEL BYTE
MAXLEN DB 20
ACCTUAL DB ?
INPUT DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

CALL CLEAR_SCREEN
CALL GET_STRING
CALL MAKE_BOX
CALL SET_CURSOR
CALL TO_LOWER
CALL DISPLAY_STRING
CALL END_PROG

END_PROG PROC
MOV AH,4CH
INT 21H
RET
END_PROG ENDP

TO_LOWER PROC
MOV SI,OFFSET STRING
MOV CH,0
MOV CL, ACCTUAL+2
AGAIN:MOV DL,[SI]
CMP DL,'A'
JB SKIP
CMP DL,'Z'
JA SKIP
ADD DL,20H
MOV [SI],DL
SKIP:INC SI
LOOP AGAIN
RET
TO_LOWER ENDP

SET_CURSOR PROC
MOV AH,02H
MOV BH,00
MOV DH,22
MOV DL,10
INT 10H
RET
SET_CURSOR ENDP

CLEAR_SCREEN PROC
MOV AH,00H
MOV AL,03H
INT 10H
RET
CLEAR_SCREEN ENDP

DISPLAY_STRING PROC
DISPLAY:MOV AH,09H
LEA DX,STRING+2
INT 21H
RET
DISPLAY_STRING ENDP

GET_STRING PROC
MOV AH, 0AH
LEA DX,STRING
INT 21H
RET
GET_STRING ENDP

MAKE_BOX PROC
MOV AH,06H
MOV AL,00
MOV BH,01011111B
MOV CH,2
MOV CL,10
MOV DH,22
MOV DL,70
INT 10H
RET
MAKE_BOX ENDP
MAIN ENDP
END MAIN
