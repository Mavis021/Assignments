TITLE to read a string and then display it in next line
.MODEL SMALL
.STACK 32
.DATA
STRING LABEL BYTE
MAXLEN  DB 20
ACCUTAL DB ?
INPUT   DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV BX,0
MOV AL,0
MOV AH,01H
STEP1:CMP AL,0DH
JZ STEP2
MOV [STRING+BX],AL
INC BX
INT 21H
JMP STEP1

STEP2: MOV CX,BX
DEC CX
MOV BX,1

;CHARACTER DISPLAY
MOV AH,02H

;NEXT LINE
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H

STEP3:MOV DL,[STRING+BX]
INT 21H
INC BX
LOOP STEP3

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
