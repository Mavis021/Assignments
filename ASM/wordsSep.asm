TITLE to read a string and then display it in next line
.MODEL SMALL
.STACK 32
.DATA
STRING LABEL BYTE
MAXLEN  DB 20
ACCTUAL DB ?
INPUT   DB 20 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,0AH
LEA DX,STRING
INT 21H

MOV BX,1
MOV CL,ACCTUAL
MOV AH,02H ;DISPLAY CHARACTERS
NLINE:MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
AGAIN:INC BX
MOV DL,[STRING+BX]
INT 21H
CMP DL,' '
JZ NLINE
LOOP AGAIN

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
