TITLE to accept the string and display it diagonally
.MODEL SMALL
.STACK 32
.DATA
COUNT DB 0
STRING LABEL BYTE
MAXLEN DB 30
ACCTUAL DB ?
INPUT  DB 30 DUP('$')

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,0AH
LEA DX,STRING
INT 21H

MOV AH,00H
MOV AL,03H
INT 10H

MOV CX,0
MOV CL,ACCTUAL
MOV BX,1
MOV DX,0
MOV AH,02H
STEP1:INC BX
MOV DL,[STRING+BX]
INT 21H
CMP DL,20H
JZ NLINE
LOOP STEP1
JMP SKIP

NLINE:MOV DL,0
ADD DL,BL
SUB DL,2
INC DH
INT 10H
JMP STEP1

SKIP:MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
