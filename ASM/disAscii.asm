TITLE to display the ascii values in a box
.MODEL SMALL
.STACK 32
.DATA

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH,00H           ;CLEAR SCREEN 
MOV AL,03H
INT 10H

MOV AH,06H    
MOV AL,00    ;scroll 00 lines up
MOV BH,00011111B
MOV CH,5
MOV CL,10
MOV DH,20
MOV DL,70
INT 10H

MOV DH,5     ;cursor
MOV DL,10
MOV CX,95
MOV BL,32
AGAIN:MOV AH,02H
MOV BH,0
INT 10H 

MOV AH,0EH
MOV AL,BL
INT 10H

INC BL
INC DL
MOV AL,0DH
INT 10H
INC DL
CMP DL,70
JNE SKIP
MOV DL,10
INC DH
SKIP:LOOP AGAIN

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
